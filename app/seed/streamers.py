def seed_streamers(db: Session) -> int:
    """Seed the database with known gaming streamers. Returns count of new entries added."""
    from app.models.streamer import Streamer
    added = 0
    for data in GAMING_STREAMERS:
        # Check for existing by twitch username, youtube channel id, or x username
        existing = None
        if data.get("twitch_username"):
            existing = db.query(Streamer).filter(Streamer.twitch_username == data["twitch_username"]).first()
        if not existing and data.get("youtube_channel_id"):
            existing = db.query(Streamer).filter(Streamer.youtube_channel_id == data["youtube_channel_id"]).first()
        if not existing and data.get("x_username"):
            existing = db.query(Streamer).filter(Streamer.x_username == data["x_username"]).first()

        if not existing:
            streamer = Streamer(**data)
            db.add(streamer)
            added += 1
    db.commit()
    return added
